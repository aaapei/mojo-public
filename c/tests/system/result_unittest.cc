// This file tests the C system result API (the macros declared in
// mojo/public/c/include/mojo/system/result.h).

#include <mojo/system/result.h>

#include "gtest/gtest.h"

namespace {

TEST(ResultTest, SystemResultMacros) {
  EXPECT_EQ(0x00000001u, MOJO_SYSTEM_RESULT_CANCELLED);
  EXPECT_EQ(0x00000002u, MOJO_SYSTEM_RESULT_UNKNOWN);
  EXPECT_EQ(0x00000003u, MOJO_SYSTEM_RESULT_INVALID_ARGUMENT);
  EXPECT_EQ(0x00000004u, MOJO_SYSTEM_RESULT_DEADLINE_EXCEEDED);
  EXPECT_EQ(0x00000005u, MOJO_SYSTEM_RESULT_NOT_FOUND);
  EXPECT_EQ(0x00000006u, MOJO_SYSTEM_RESULT_ALREADY_EXISTS);
  EXPECT_EQ(0x00000007u, MOJO_SYSTEM_RESULT_PERMISSION_DENIED);
  EXPECT_EQ(0x00000008u, MOJO_SYSTEM_RESULT_RESOURCE_EXHAUSTED);
  EXPECT_EQ(0x00000009u, MOJO_SYSTEM_RESULT_FAILED_PRECONDITION);
  EXPECT_EQ(0x00000019u, MOJO_SYSTEM_RESULT_BUSY);
  EXPECT_EQ(0x0000000au, MOJO_SYSTEM_RESULT_ABORTED);
  EXPECT_EQ(0x0000000bu, MOJO_SYSTEM_RESULT_OUT_OF_RANGE);
  EXPECT_EQ(0x0000000cu, MOJO_SYSTEM_RESULT_UNIMPLEMENTED);
  EXPECT_EQ(0x0000000du, MOJO_SYSTEM_RESULT_INTERNAL);
  EXPECT_EQ(0x0000000eu, MOJO_SYSTEM_RESULT_UNAVAILABLE);
  EXPECT_EQ(0x0000001eu, MOJO_SYSTEM_RESULT_SHOULD_WAIT);
  EXPECT_EQ(0x0000000fu, MOJO_SYSTEM_RESULT_DATA_LOSS);
}

}  // namespace

